package entity.ghost;



/**
 * ממשק המגדיר את ההתנהגות של בלינקי במצב נתון.
 * כל קלאס שיממש ממשק זה ייצג מצב אחר במכונת המצבים של בלינקי.
 */
public interface BlinkyState {

    /**
     * מתודת העדכון הראשית של המצב.
     * הלוגיקה הספציפית למצב זה תמומש כאן.
     * @param blinky ה"הקשר" (Context) - האובייקט של בלינקי עצמו,
     * כדי לגשת למשתנים שלו ולשנות את מצבו.
     */
    void update(Blinky blinky);
}